#include <stdio.h>
int main(){
    int a=5,b=5,c=5,d=5;
    // prefix increment
    printf("a=%d\n",++a);
    // postfix increment
    printf("b=%d\n",b++);
    // prefix decrement
    printf("c=%d\n",--c);
    // postfix decrement
    printf("d=%d\n",d--);
    return 0;
}
